
public class Aluno {
	String nome;
	String numeroMatricula;
	
	public Aluno(String nome, String numeroMatricula) {
		super();
		this.nome = nome;
		this.numeroMatricula = numeroMatricula;
	}
	
	public String getNome() {
		return nome;
	}
	public String getNumeroMatricula() {
		return numeroMatricula;
	}
	
}
